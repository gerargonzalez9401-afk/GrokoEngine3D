    private static string PreviewColorExpression(string expr, PinType type)
    {
        return type switch
        {
            PinType.Float => $"vec3({expr})",
            PinType.Vec2 => $"vec3(({expr}).xy, 0.0)",
            PinType.Vec3 => expr,
            PinType.Vec4 => $"({expr}).rgb",
            _ => "vec3(0.0)"
        };
    }

    public string GenerateVertexShader()
    {
        return """
#version 330 core

// Generated by Groko Shader Graph Pro - vertex shader base.
layout(location = 0) in vec3 a_Position;
layout(location = 1) in vec3 a_Normal;
layout(location = 2) in vec2 a_UV;

uniform mat4 u_Model;
uniform mat4 u_View;
uniform mat4 u_Projection;

out vec2 v_UV;
out vec3 v_NormalWS;
out vec3 v_WorldPos;
out vec3 v_ObjectPos;
out vec3 v_TangentWS;
out vec4 v_ScreenPos;

void main()
{
    vec4 world = u_Model * vec4(a_Position, 1.0);
    v_WorldPos = world.xyz;
    v_ObjectPos = a_Position;
    v_NormalWS = normalize(mat3(u_Model) * a_Normal);
    vec3 tangentOS = abs(a_Normal.y) > 0.99 ? vec3(1.0, 0.0, 0.0) : normalize(cross(vec3(0.0, 1.0, 0.0), a_Normal));
    v_TangentWS = normalize(mat3(u_Model) * tangentOS);
    v_UV = a_UV;
    gl_Position = u_Projection * u_View * world;
    v_ScreenPos = gl_Position;
}
""";
    }

    public string GenerateHlslFragmentShader(ShaderGraphModel graph)
    {
        graph.Normalize();
        ShaderGraphSchemaRepair.Repair(graph);
        ShaderGraphDynamicTypes.Synchronize(graph);
        _graph = graph;
        _cache.Clear();
        _guard.Clear();
        _features.Clear();

        var output = graph.Nodes.FirstOrDefault(n => n.Kind == NodeKind.Output);
        var baseColor = ToHlslExpression(BuildOutputInput(output, "Base Color", PinType.Vec3, "vec3(1.0, 0.0, 1.0)"));
        var normal = ToHlslExpression(BuildOutputInput(output, "Normal", PinType.Vec3, "normalize(v_NormalWS)"));
        var emission = ToHlslExpression(BuildOutputInput(output, "Emission", PinType.Vec3, "vec3(0.0)"));
        var alpha = ToHlslExpression(BuildOutputInput(output, "Alpha", PinType.Float, "1.0"));
        var alphaClip = ToHlslExpression(BuildOutputInput(output, "Alpha Clip", PinType.Float, "0.0"));
        var metallic = ToHlslExpression(BuildOutputInput(output, "Metallic", PinType.Float, "0.0"));
        var roughness = ToHlslExpression(BuildOutputRoughnessInput(output));
        var ambientOcclusion = ToHlslExpression(BuildOutputInput(output, "Ambient Occlusion", PinType.Float, "1.0"));
        var specular = ToHlslExpression(BuildOutputInput(output, "Specular", PinType.Float, "0.5"));
        var clearCoat = ToHlslExpression(BuildOutputInput(output, "Clear Coat", PinType.Float, "0.0"));
        var clearCoatRoughness = ToHlslExpression(BuildOutputInput(output, "Clear Coat Roughness", PinType.Float, "0.1"));

        var sb = new StringBuilder();
        sb.AppendLine("// HLSL generated by Groko Shader Graph Pro GOD MODE");
        sb.AppendLine("// Pixel shader real, not a GLSL comment dump.");
        sb.AppendLine();
        sb.AppendLine("cbuffer GrokoMaterial : register(b0)");
        sb.AppendLine("{");
        sb.AppendLine("    float u_Time;");
        sb.AppendLine("    float2 u_Resolution;");
        sb.AppendLine("    float3 u_CameraPos;");
        sb.AppendLine("    float _GrokoPadding0;");
        sb.AppendLine("    float3 u_LightDir;");
        sb.AppendLine("    float _GrokoPadding1;");
        if (_features.Contains("scenedepth"))
        {
            sb.AppendLine("    float u_CameraNear;");
            sb.AppendLine("    float u_CameraFar;");
            sb.AppendLine("    float2 _GrokoPadding2;");
        }
        foreach (var uniform in MaterialPropertyUniforms())
            sb.AppendLine("    " + ToHlslUniformField(uniform));
        sb.AppendLine("};");
        sb.AppendLine();

        var slot = 0;
        foreach (var sampler in TextureSamplerUniforms())
        {
            sb.AppendLine($"Texture2D {sampler} : register(t{slot});");
            slot++;
        }
        if (_features.Contains("scenedepth"))
            sb.AppendLine($"Texture2D u_SceneDepth : register(t{slot});");
        sb.AppendLine("SamplerState GrokoLinearSampler : register(s0);");
        sb.AppendLine();
        sb.AppendLine("cbuffer GrokoObject : register(b1)");
        sb.AppendLine("{");
        sb.AppendLine("    float4x4 u_Model;");
        sb.AppendLine("    float4x4 u_View;");
        sb.AppendLine("    float4x4 u_Projection;");
        sb.AppendLine("};");
        sb.AppendLine();
        sb.AppendLine("struct VSInput");
        sb.AppendLine("{");
        sb.AppendLine("    float3 PositionOS : POSITION;");
        sb.AppendLine("    float3 NormalOS : NORMAL;");
        sb.AppendLine("    float2 UV : TEXCOORD0;");
        sb.AppendLine("};");
        sb.AppendLine();
        sb.AppendLine("struct PSInput");
        sb.AppendLine("{");
        sb.AppendLine("    float4 PositionCS : SV_POSITION;");
        sb.AppendLine("    float2 v_UV : TEXCOORD0;");
        sb.AppendLine("    float3 v_NormalWS : TEXCOORD1;");
        sb.AppendLine("    float3 v_WorldPos : TEXCOORD2;");
        sb.AppendLine("    float3 v_ObjectPos : TEXCOORD3;");
        sb.AppendLine("    float3 v_TangentWS : TEXCOORD4;");
        sb.AppendLine("    float4 v_ScreenPos : TEXCOORD5;");
        sb.AppendLine("};");
        sb.AppendLine();
        sb.AppendLine("PSInput VSMain(VSInput input)");
        sb.AppendLine("{");
        sb.AppendLine("    PSInput o;");
        sb.AppendLine("    float4 world = mul(float4(input.PositionOS, 1.0), u_Model);");
        sb.AppendLine("    o.v_WorldPos = world.xyz;");
        sb.AppendLine("    o.v_ObjectPos = input.PositionOS;");
        sb.AppendLine("    o.v_NormalWS = normalize(mul(float4(input.NormalOS, 0.0), u_Model).xyz);");
        sb.AppendLine("    o.v_TangentWS = normalize(mul(float4(1.0, 0.0, 0.0, 0.0), u_Model).xyz);");
        sb.AppendLine("    o.v_UV = input.UV;");
        sb.AppendLine("    o.PositionCS = mul(mul(world, u_View), u_Projection);");
        sb.AppendLine("    o.v_ScreenPos = o.PositionCS;");
        sb.AppendLine("    return o;");
        sb.AppendLine("}");
        sb.AppendLine();
        AppendHlslFeatureFunctions(sb);
        sb.AppendLine("float4 PSMain(PSInput input) : SV_Target");
        sb.AppendLine("{");
        sb.AppendLine("    float2 v_UV = input.v_UV;");
        sb.AppendLine("    float3 v_NormalWS = input.v_NormalWS;");
        sb.AppendLine("    float3 v_WorldPos = input.v_WorldPos;");
        sb.AppendLine("    float3 v_ObjectPos = input.v_ObjectPos;");
        sb.AppendLine("    float3 v_TangentWS = input.v_TangentWS;");
        sb.AppendLine("    float4 v_ScreenPos = input.v_ScreenPos;");
        if (_features.Contains("scenedepth"))
            sb.AppendLine("    float v_FragDepth = input.PositionCS.z;");
        sb.AppendLine($"    float3 baseColor = {baseColor};");
        sb.AppendLine($"    float3 normalWS = normalize({normal});");
        sb.AppendLine($"    float3 emission = {emission};");
        sb.AppendLine($"    float alpha = saturate({alpha});");
        sb.AppendLine($"    float alphaClip = {alphaClip};");
        sb.AppendLine($"    float metallic = saturate({metallic});");
        sb.AppendLine($"    float roughness = clamp({roughness}, 0.02, 1.0);");
        sb.AppendLine($"    float ambientOcclusion = saturate({ambientOcclusion});");
        sb.AppendLine($"    float specular = saturate({specular});");
        sb.AppendLine($"    float clearCoat = saturate({clearCoat});");
        sb.AppendLine($"    float clearCoatRoughness = clamp({clearCoatRoughness}, 0.02, 1.0);");
        sb.AppendLine("    if (alphaClip > 0.0 && alpha < alphaClip) discard;");
        sb.AppendLine("    float3 N = normalize(normalWS);");
        sb.AppendLine("    float3 V = normalize(u_CameraPos - v_WorldPos);");
        sb.AppendLine("    float3 L = normalize(-u_LightDir);");
        sb.AppendLine("    float3 H = normalize(V + L);");
        sb.AppendLine("    float NdotL = saturate(dot(N, L));");
        sb.AppendLine("    float NdotV = saturate(dot(N, V));");
        sb.AppendLine("    float3 F0 = lerp(float3(0.04 + specular * 0.08, 0.04 + specular * 0.08, 0.04 + specular * 0.08), baseColor, metallic);");
        sb.AppendLine("    float3 F = fresnelSchlick(max(dot(H, V), 0.0), F0);");
        sb.AppendLine("    float D = DistributionGGX(N, H, roughness);");
        sb.AppendLine("    float G = GeometrySmith(N, V, L, roughness);");
        sb.AppendLine("    float3 specTerm = (D * G * F) / max(4.0 * NdotV * NdotL, 0.0001);");
        sb.AppendLine("    float3 kS = F;");
        sb.AppendLine("    float3 kD = (1.0 - kS) * (1.0 - metallic);");
        sb.AppendLine("    float3 diffuse = baseColor * (1.0 - metallic);");
        sb.AppendLine("    float3 ambient = diffuse * ambientOcclusion * (0.18 + 0.22 * saturate(N.y * 0.5 + 0.5));");
        sb.AppendLine("    float rim = pow(1.0 - saturate(dot(N, V)), lerp(8.0, 2.0, clearCoatRoughness)) * clearCoat;");
        sb.AppendLine("    float3 finalColor = clamp(ambient + (kD * diffuse / 3.14159265 + specTerm) * NdotL + float3(rim * 0.18, rim * 0.18, rim * 0.18) + emission, float3(0.0, 0.0, 0.0), float3(64.0, 64.0, 64.0));");
        sb.AppendLine("    return float4(finalColor, alpha);");
        sb.AppendLine("}");
        return sb.ToString();
    }

    private string BuildOutputInput(ShaderNode? output, string pinName, PinType type, string fallback)
    {
        var pin = output?.Input(pinName);
        if (pin is null && pinName.Equals("Base Color", StringComparison.OrdinalIgnoreCase))
            pin = output?.Input("Color"); // legacy graph compatibility

        if (pin is null)
            return fallback;

        // Los graphs viejos guardaban el Normal del Master como vec3(0,0,1),
        // lo que hacía que Sphere/Cube/Plane se vieran planos en OpenGL.
        // Si no hay cable conectado al Normal, usamos la normal real de la malla.
        if (pinName.Equals("Normal", StringComparison.OrdinalIgnoreCase)
            && _graph.FindConnectionToInput(pin.Id) is null)
        {
            return fallback;
        }

        return BuildInput(pin, type);
    }

    private string BuildOutputRoughnessInput(ShaderNode? output)
    {
        var smoothness = output?.Input("Smoothness");
        if (smoothness is not null && _graph.FindConnectionToInput(smoothness.Id) is not null)
            return $"(1.0 - clamp({BuildInput(smoothness, PinType.Float)}, 0.0, 1.0))";

        return BuildOutputInput(output, "Roughness", PinType.Float, "0.5");
    }

    private string BuildInput(GraphPin? inputPin, PinType expectedType)
    {
        if (inputPin is null)
            return DefaultForType(expectedType);

        var connection = _graph.FindConnectionToInput(inputPin.Id);
        if (connection is null)
            return CastExpression(inputPin.DefaultValue, inputPin.Type, expectedType);

        var sourcePin = _graph.FindPin(connection.FromPinId);
        var sourceNode = _graph.FindNodeByPin(connection.FromPinId);
        if (sourcePin is null || sourceNode is null)
            return CastExpression(inputPin.DefaultValue, inputPin.Type, expectedType);

        return CastExpression(BuildOutput(sourceNode, sourcePin), ResolveOutputType(sourceNode, sourcePin), expectedType);
    }

    private static GraphPin? InputAny(ShaderNode node, params string[] names)
    {
        foreach (var name in names)
        {
            var pin = node.Input(name);
            if (pin is not null)
                return pin;
        }

        return null;
    }

    private bool IsInputConnectedFrom(GraphPin? inputPin, NodeKind kind)
    {
        if (inputPin is null)
            return false;

        var connection = _graph.FindConnectionToInput(inputPin.Id);
        return connection is not null
            && _graph.FindNodeByPin(connection.FromPinId)?.Kind == kind;
    }

    private string BuildOutput(ShaderNode node, GraphPin outputPin)
    {
        if (_cache.TryGetValue(outputPin.Id, out var cached))
            return cached;

        if (!_guard.Add(outputPin.Id))
            return DefaultForType(outputPin.Type);

        string expression = node.Kind switch
        {
            NodeKind.ConstantFloat => F(node.FloatValue),
            NodeKind.ConstantColor => ToVec3(node.ColorHex, node.ColorIntensity),
            NodeKind.ConstantVector2 => ToVec2(node.TextValue),
            NodeKind.ConstantVector3 => ToVec3FromText(node.TextValue),
            NodeKind.Time => BuildTime(outputPin),
            NodeKind.TextureCoord => "v_UV",
            NodeKind.TangentVector => "normalize(v_TangentWS)",
            NodeKind.CameraVector => "normalize(u_CameraPos - v_WorldPos)",
            NodeKind.ObjectPosition => "v_ObjectPos",
            NodeKind.ScreenPosition => BuildScreenPosition(node, outputPin),
            NodeKind.PropertyFloat => SanitizeIdentifier(PropertyUniformName(node)),
            NodeKind.PropertyColor => BuildPropertyColorOutput(node),
            NodeKind.PropertyVector2 => SanitizeIdentifier(PropertyUniformName(node)),
            NodeKind.PropertyVector3 => SanitizeIdentifier(PropertyUniformName(node)),
            NodeKind.PropertyVector4 => SanitizeIdentifier(PropertyUniformName(node)),
            NodeKind.PropertyTexture2D => SanitizeIdentifier(PropertyUniformName(node)),
            NodeKind.TextureSample => BuildTextureSample(node, outputPin),
            NodeKind.Flipbook => BuildFlipbook(node),
            NodeKind.NormalVector => "normalize(v_NormalWS)",
            NodeKind.ViewDirection => "normalize(u_CameraPos - v_WorldPos)",
            NodeKind.WorldPosition => "v_WorldPos",
            NodeKind.Add => BuildBinaryAuto(node, outputPin, "+"),
            NodeKind.Subtract => BuildBinaryAuto(node, outputPin, "-"),
            NodeKind.Multiply => BuildBinaryAuto(node, outputPin, "*"),
            NodeKind.Divide => BuildDivide(node, outputPin),
            NodeKind.Power => BuildPower(node),
            NodeKind.Sin => BuildUnaryAuto(node, "sin"),
            NodeKind.Cos => BuildUnaryAuto(node, "cos"),
            NodeKind.Clamp => BuildClamp(node),
            NodeKind.Smoothstep => BuildSmoothstep(node),
            NodeKind.Remap => BuildRemap(node),
            NodeKind.OneMinus => BuildOneMinus(node),
            NodeKind.Posterize => BuildPosterize(node),
            NodeKind.Length => BuildLength(node),
            NodeKind.Saturate => BuildSaturate(node),
            NodeKind.Step => BuildStep(node),
            NodeKind.Abs => BuildUnaryAuto(node, "abs"),
            NodeKind.Floor => BuildUnaryAuto(node, "floor"),
            NodeKind.Ceil => BuildUnaryAuto(node, "ceil"),
            NodeKind.Fraction => BuildUnaryAuto(node, "fract"),
            NodeKind.Min => BuildMinMax(node, outputPin, "min"),
