    private static string PropertyUniformName(ShaderNode node)
    {
        var value = string.IsNullOrWhiteSpace(node.TextValue) ? node.Title : node.TextValue.Trim();
        return value.StartsWith("u_", StringComparison.OrdinalIgnoreCase) ? value : "u_" + value;
    }

    private static string TextureUniformName(ShaderNode node)
    {
        var value = string.IsNullOrWhiteSpace(node.TextValue) ? string.Empty : node.TextValue.Trim();
        if (!string.IsNullOrWhiteSpace(node.TexturePath) && IsDefaultTextureUniformName(value))
            return LocalTextureUniformName(node);

        if (string.IsNullOrWhiteSpace(value))
            value = "u_MainTex";

        var fileName = Path.GetFileNameWithoutExtension(value);
        if (!string.IsNullOrWhiteSpace(fileName) && (value.Contains('\\') || value.Contains('/') || value.Contains('.')))
            return "u_" + fileName;
        return value;
    }

    private static bool IsDefaultTextureUniformName(string? value)
    {
        var v = (value ?? string.Empty).Trim();
        if (string.IsNullOrWhiteSpace(v))
            return true;

        return v.Equals("MainTex", StringComparison.OrdinalIgnoreCase)
            || v.Equals("u_MainTex", StringComparison.OrdinalIgnoreCase)
            || v.Equals("NormalMap", StringComparison.OrdinalIgnoreCase)
            || v.Equals("u_NormalMap", StringComparison.OrdinalIgnoreCase)
            || v.Equals("MetallicMap", StringComparison.OrdinalIgnoreCase)
            || v.Equals("u_MetallicMap", StringComparison.OrdinalIgnoreCase)
            || v.Equals("SmoothnessMap", StringComparison.OrdinalIgnoreCase)
            || v.Equals("u_SmoothnessMap", StringComparison.OrdinalIgnoreCase)
            || v.Equals("AOMap", StringComparison.OrdinalIgnoreCase)
            || v.Equals("u_AOMap", StringComparison.OrdinalIgnoreCase);
    }

    private static string LocalTextureUniformName(ShaderNode node)
    {
        var prefix = node.Kind switch
        {
            NodeKind.NormalMap => "u_NormalMap_",
            NodeKind.MetallicMap => "u_MetallicMap_",
            NodeKind.SmoothnessMap => "u_SmoothnessMap_",
            NodeKind.AmbientOcclusionMap => "u_AOMap_",
            NodeKind.Triplanar => "u_Triplanar_",
            _ => "u_Texture_"
        };

        return prefix + node.Id.ToString("N")[..8];
    }

    private IEnumerable<string> TextureSamplerUniforms()
    {
        var direct = _graph.Nodes
            .Where(n => n.Kind == NodeKind.TextureSample || n.Kind == NodeKind.NormalMap || n.Kind == NodeKind.Triplanar
                        || n.Kind == NodeKind.MetallicMap || n.Kind == NodeKind.SmoothnessMap || n.Kind == NodeKind.AmbientOcclusionMap)
            .Select(n => SanitizeIdentifier(TextureUniformName(n)));

        var nodeProperties = _graph.Nodes
            .Where(n => n.Kind == NodeKind.PropertyTexture2D)
            .Select(n => SanitizeIdentifier(PropertyUniformName(n)));

        var blackboardProperties = (_graph.Properties ?? new List<GraphProperty>())
            .Where(p => p.Type == PinType.Texture2D)
            .Select(p => SanitizeIdentifier(p.UniformName));

        return direct.Concat(nodeProperties).Concat(blackboardProperties).Distinct(StringComparer.OrdinalIgnoreCase);
    }

    private IEnumerable<string> MaterialPropertyUniforms()
    {
        var uniforms = new List<string>();
        var declared = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

        // Los samplers (Texture2D) ya ocupan estos nombres; un uniform numérico con el
        // mismo nombre produciría una redeclaración inválida en GLSL/HLSL.
        var textureNames = new HashSet<string>(TextureSamplerUniforms(), StringComparer.OrdinalIgnoreCase);
        foreach (var name in textureNames)
            declared.Add(name);

        // Las propiedades del Blackboard tienen prioridad: si un nodo Property* del
        // canvas referencia el mismo nombre, no debe redeclarar el uniform con otro tipo.
        foreach (var property in _graph.Properties ?? new List<GraphProperty>())
        {
            var name = SanitizeIdentifier(property.UniformName);
            if (!declared.Add(name)) continue;
            switch (property.Type)
            {
                case PinType.Float:
                    uniforms.Add($"uniform float {name};");
                    break;
                case PinType.Vec2:
                    uniforms.Add($"uniform vec2 {name};");
                    break;
                case PinType.Vec3:
                    uniforms.Add($"uniform vec3 {name};");
                    break;
                case PinType.Vec4:
                    uniforms.Add($"uniform vec4 {name};");
                    break;
            }
        }

        foreach (var node in _graph.Nodes)
        {
            var name = SanitizeIdentifier(PropertyUniformName(node));
            if (!declared.Add(name)) continue;
            switch (node.Kind)
            {
                case NodeKind.PropertyFloat:
                    uniforms.Add($"uniform float {name};");
                    break;
                case NodeKind.PropertyColor:
                case NodeKind.PropertyVector3:
                    uniforms.Add($"uniform vec3 {name};");
                    break;
                case NodeKind.PropertyVector4:
                    uniforms.Add($"uniform vec4 {name};");
                    break;
                case NodeKind.PropertyVector2:
                    uniforms.Add($"uniform vec2 {name};");
                    break;
            }
        }

        return uniforms;
    }

    private static string SanitizeIdentifier(string value)
    {
        var chars = value.Select((c, i) => (char.IsLetter(c) || c == '_' || (i > 0 && char.IsDigit(c))) ? c : '_').ToArray();
        var result = new string(chars);
        return string.IsNullOrWhiteSpace(result) ? "u_MainTex" : result;
    }

    private string BuildBinaryAuto(ShaderNode node, GraphPin outputPin, string op)
    {
        var aPin = node.Input("A")!;
        var bPin = node.Input("B")!;
        var type = HighestNumericType(ResolveInputType(aPin), ResolveInputType(bPin), outputPin.Type);
        var a = BuildInput(aPin, type);
        var b = BuildInput(bPin, type);
        return $"({a} {op} {b})";
    }

    private string BuildDivide(ShaderNode node, GraphPin outputPin)
    {
        var aPin = node.Input("A")!;
        var bPin = node.Input("B")!;
        var type = HighestNumericType(ResolveInputType(aPin), ResolveInputType(bPin), outputPin.Type);
        var a = BuildInput(aPin, type);
        var b = BuildInput(bPin, type);
        var safeB = type == PinType.Float
            ? $"max(abs({b}), 0.00001)"
            : $"max(abs({b}), {ConstructorForType(type, "0.00001")})";
        return $"({a} / {safeB})";
    }

