    private string BuildTextureSample(ShaderNode node, GraphPin outputPin)
    {
        var texturePin = node.Input("Texture");
        var uv = BuildInput(node.Input("UV")!, PinType.Vec2);
        var tilingPin = node.Input("Tiling");
        var offsetPin = node.Input("Offset");
        var tiling = tilingPin is null ? "vec2(1.0, 1.0)" : BuildInput(tilingPin, PinType.Vec2);
        var offset = offsetPin is null ? "vec2(0.0, 0.0)" : BuildInput(offsetPin, PinType.Vec2);
        var sampleUv = $"(({uv}) * {tiling} + {offset})";
        var samplerName = texturePin is null ? SanitizeIdentifier(TextureUniformName(node)) : BuildInput(texturePin, PinType.Texture2D);
        samplerName = SanitizeIdentifier(samplerName);
        var sample = $"texture({samplerName}, {sampleUv})";
        var rgb = $"{sample}.rgb";
        // Las texturas de color (albedo) vienen codificadas en sRGB; se decodifican a
        // lineal igual que en el shader de objetos sólidos para que se vean iguales.
        var linearRgb = node.TextureSettings.SRgb && !node.TextureSettings.IsNormalMap
            ? $"(u_ColorSpaceLinear == 1 ? pow({rgb}, vec3(2.2)) : {rgb})"
            : rgb;
        var outputRgb = node.TextureSettings.IsNormalMap
            ? $"normalize(({rgb}) * 2.0 - 1.0)"
            : linearRgb;

        return outputPin.Name.ToLowerInvariant() switch
        {
            "rgba" => $"vec4({outputRgb}, {sample}.a)",
            "rgb" => outputRgb,
            "r" => $"{sample}.r",
            "g" => $"{sample}.g",
            "b" => $"{sample}.b",
            "a" or "alpha" => $"{sample}.a",
            _ => $"{sample}.rgb"
        };
    }

    private string BuildScreenPosition(ShaderNode node, GraphPin outputPin)
    {
        var uv = "((v_ScreenPos.xy / max(v_ScreenPos.w, 0.00001)) * 0.5 + 0.5)";
        string RawScreenPosition()
        {
            _features.Add("scenedepth");
            return "vec4(v_ScreenPos.xy, v_ScreenPos.z, sceneDepthLinearEye(v_FragDepth))";
        }

        if (outputPin.Name.Equals("UV", StringComparison.OrdinalIgnoreCase))
            return uv;
        if (outputPin.Name.Equals("Raw", StringComparison.OrdinalIgnoreCase))
            return RawScreenPosition();

        var mode = string.IsNullOrWhiteSpace(node.TextValue) ? "Raw" : node.TextValue.Trim();
        return mode.ToLowerInvariant() switch
        {
            "raw" => RawScreenPosition(),
            "center" => $"vec4(({uv}) * 2.0 - 1.0, 0.0, 1.0)",
            "tiled" => $"vec4(fract({uv}), 0.0, 1.0)",
            "pixel" => "vec4(v_ScreenPos.xy, v_ScreenPos.zw)",
            _ => $"vec4({uv}, 0.0, 1.0)"
        };
    }

