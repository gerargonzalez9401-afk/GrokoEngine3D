    public string GenerateFragmentShader(ShaderGraphModel graph)
    {
        graph.Normalize();
        ShaderGraphSchemaRepair.Repair(graph);
        ShaderGraphDynamicTypes.Synchronize(graph);
        _graph = graph;
        _cache.Clear();
        _guard.Clear();
        _features.Clear();

        var output = graph.Nodes.FirstOrDefault(n => n.Kind == NodeKind.Output);
        var baseColor = BuildOutputInput(output, "Base Color", PinType.Vec3, "vec3(1.0, 0.0, 1.0)");
        var normal = BuildOutputInput(output, "Normal", PinType.Vec3, "normalize(v_NormalWS)");
        var emission = BuildOutputInput(output, "Emission", PinType.Vec3, "vec3(0.0)");
        var alpha = BuildOutputInput(output, "Alpha", PinType.Float, "1.0");
        var alphaClip = BuildOutputInput(output, "Alpha Clip", PinType.Float, "0.0");
        var metallic = BuildOutputInput(output, "Metallic", PinType.Float, "0.0");
        var roughness = BuildOutputRoughnessInput(output);
        var ambientOcclusion = BuildOutputInput(output, "Ambient Occlusion", PinType.Float, "1.0");
        var specular = BuildOutputInput(output, "Specular", PinType.Float, "0.5");
        var clearCoat = BuildOutputInput(output, "Clear Coat", PinType.Float, "0.0");
        var clearCoatRoughness = BuildOutputInput(output, "Clear Coat Roughness", PinType.Float, "0.1");

        var sb = new StringBuilder();
        sb.AppendLine("#version 330 core");
        sb.AppendLine();
        sb.AppendLine("// Generated by Groko Shader Graph Pro GOD MODE");
        sb.AppendLine("// External tool first. Later this file can be integrated into GrokoEngine.");
        sb.AppendLine();
        sb.AppendLine("in vec2 v_UV;");
        sb.AppendLine("in vec3 v_NormalWS;");
        sb.AppendLine("in vec3 v_WorldPos;");
        sb.AppendLine("in vec3 v_ObjectPos;");
        sb.AppendLine("in vec3 v_TangentWS;");
        sb.AppendLine("in vec4 v_ScreenPos;");
        sb.AppendLine("out vec4 FragColor;");
        sb.AppendLine();
        sb.AppendLine("uniform float u_Time;");
        sb.AppendLine("uniform vec2 u_Resolution;");
        sb.AppendLine("uniform vec3 u_CameraPos;");
        sb.AppendLine("uniform vec3 u_LightDir;");
        sb.AppendLine("uniform vec3 u_LightColor;");
        sb.AppendLine("uniform float u_LightIntensity;");
        sb.AppendLine("uniform int u_ColorSpaceLinear;");
        // Sombras direccionales por cascadas (recibidas, igual que el material estándar).
        sb.AppendLine("uniform sampler2DArray u_ShadowMap;");
        sb.AppendLine("uniform int u_ShadowEnabled;");
        sb.AppendLine("uniform float u_ShadowStrength;");
        sb.AppendLine("uniform int u_CascadeCount;");
        sb.AppendLine("uniform mat4 u_CascadeLightMvp[5];");
        sb.AppendLine("uniform float u_CascadeSplit[5];");
        sb.AppendLine("uniform int u_ShadowPcfRadius;");
        sb.AppendLine("uniform float u_ShadowBiasScale;");
        sb.AppendLine("uniform vec3 u_ShadowCameraPos;");
        if (_features.Contains("scenedepth"))
        {
            sb.AppendLine("uniform sampler2D u_SceneDepth;");
            sb.AppendLine("uniform float u_CameraNear;");
            sb.AppendLine("uniform float u_CameraFar;");
        }
        foreach (var uniform in MaterialPropertyUniforms())
            sb.AppendLine(uniform);
        foreach (var sampler in TextureSamplerUniforms())
            sb.AppendLine($"uniform sampler2D {sampler};");
        sb.AppendLine();

        AppendFeatureFunctions(sb);

        sb.AppendLine("void main()");
        sb.AppendLine("{");
        if (_features.Contains("scenedepth"))
            sb.AppendLine("    float v_FragDepth = gl_FragCoord.z;");
        sb.AppendLine($"    vec3 baseColor = {baseColor};");
        sb.AppendLine($"    vec3 normalWS = normalize({normal});");
        sb.AppendLine($"    vec3 emission = {emission};");
        sb.AppendLine($"    float alpha = clamp({alpha}, 0.0, 1.0);");
        sb.AppendLine($"    float alphaClip = {alphaClip};");
        sb.AppendLine($"    float metallic = clamp({metallic}, 0.0, 1.0);");
        sb.AppendLine($"    float roughness = clamp({roughness}, 0.02, 1.0);");
        sb.AppendLine($"    float ambientOcclusion = clamp({ambientOcclusion}, 0.0, 1.0);");
        sb.AppendLine($"    float specular = clamp({specular}, 0.0, 1.0);");
        sb.AppendLine($"    float clearCoat = clamp({clearCoat}, 0.0, 1.0);");
        sb.AppendLine($"    float clearCoatRoughness = clamp({clearCoatRoughness}, 0.02, 1.0);");
        sb.AppendLine("    if (alphaClip > 0.0 && alpha < alphaClip) discard;");
        sb.AppendLine("    // PBR-lite output with main directional light, Schlick Fresnel and GGX-like specular.");
        sb.AppendLine("    vec3 N = normalize(normalWS);");
        sb.AppendLine("    vec3 V = normalize(u_CameraPos - v_WorldPos);");
        sb.AppendLine("    vec3 L = normalize(-u_LightDir);");
        sb.AppendLine("    vec3 H = normalize(V + L);");
        sb.AppendLine("    float NdotL = max(dot(N, L), 0.0);");
        sb.AppendLine("    float NdotV = max(dot(N, V), 0.0);");
        sb.AppendLine("    vec3 F0 = mix(vec3(0.04 + specular * 0.08), baseColor, metallic);");
        sb.AppendLine("    vec3 F = fresnelSchlick(max(dot(H, V), 0.0), F0);");
        sb.AppendLine("    float D = DistributionGGX(N, H, roughness);");
        sb.AppendLine("    float G = GeometrySmith(N, V, L, roughness);");
        sb.AppendLine("    vec3 specularTerm = (D * G * F) / max(4.0 * NdotV * NdotL, 0.0001);");
        sb.AppendLine("    vec3 kS = F;");
        sb.AppendLine("    vec3 kD = (vec3(1.0) - kS) * (1.0 - metallic);");
        sb.AppendLine("    vec3 diffuseColor = baseColor * (1.0 - metallic);");
        sb.AppendLine("    vec3 ambient = diffuseColor * ambientOcclusion * (0.18 + 0.22 * clamp(N.y * 0.5 + 0.5, 0.0, 1.0));");
        sb.AppendLine("    vec3 radiance = u_LightColor * u_LightIntensity;");
        sb.AppendLine("    float shadowReceive = ReceiveShadow(N, u_LightDir);");
        sb.AppendLine("    vec3 direct = (kD * diffuseColor / 3.14159265 + specularTerm) * NdotL * radiance * shadowReceive;");
        sb.AppendLine("    float rim = pow(1.0 - max(dot(N, V), 0.0), mix(8.0, 2.0, clearCoatRoughness)) * clearCoat;");
        sb.AppendLine("    vec3 finalColor = clamp(ambient + direct + vec3(rim * 0.18) + emission, vec3(0.0), vec3(64.0));");
        sb.AppendLine("    FragColor = vec4(finalColor, alpha);");
        sb.AppendLine("}");
        return sb.ToString();
    }

