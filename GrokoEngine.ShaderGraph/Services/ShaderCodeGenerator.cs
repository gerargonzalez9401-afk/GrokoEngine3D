    private string BuildRemap(ShaderNode node)
    {
        var inputPin = node.Input("In") ?? node.Input("X");
        var inRangePin = node.Input("In Min Max") ?? node.Input("From Min") ?? node.Input("From Max");
        var outRangePin = node.Input("Out Min Max") ?? node.Input("To Min") ?? node.Input("To Max");

        var input = BuildInput(inputPin, PinType.Float);
        var inRange = BuildInput(inRangePin, PinType.Vec2);
        var outRange = BuildInput(outRangePin, PinType.Vec2);

        return $"mix(({outRange}).x, ({outRange}).y, clamp(({input} - ({inRange}).x) / max(abs(({inRange}).y - ({inRange}).x), 0.00001), 0.0, 1.0))";
    }

