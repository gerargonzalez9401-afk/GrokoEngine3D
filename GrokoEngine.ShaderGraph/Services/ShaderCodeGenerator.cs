    private string BuildSubGraph(ShaderNode node, GraphPin outputPin)
    {
        var assetName = string.IsNullOrWhiteSpace(node.TextValue) ? node.Title : node.TextValue.Trim();
        var asset = _graph.SubGraphs.FirstOrDefault(s =>
            s.Name.Equals(assetName, StringComparison.OrdinalIgnoreCase) ||
            s.Id.ToString().Equals(assetName, StringComparison.OrdinalIgnoreCase));

        if (asset is not null && asset.Nodes.Count > 0)
        {
            // Inline real del sub-grafo guardado: se evalúa el Master Output interno y se
            // inserta su expresión en el shader principal. Como este generador es expresional,
            // no genera variables temporales que puedan colisionar. Los SubGraph inputs externos
            // caen al passthrough seguro de abajo si el asset todavía no define salida completa.
            var previousGraph = _graph;
            try
            {
                var subGraphModel = new ShaderGraphModel
                {
                    Name = asset.Name,
                    Nodes = asset.Nodes,
                    Connections = asset.Connections,
                    Properties = previousGraph.Properties,
                    SubGraphs = previousGraph.SubGraphs
                };
                _graph = subGraphModel;
                var output = subGraphModel.Nodes.FirstOrDefault(n => n.Kind == NodeKind.Output);
                if (output is not null)
                {
                    var pinName = outputPin.Type switch
                    {
                        PinType.Float => "Alpha",
                        PinType.Vec2 => "UV",
                        PinType.Vec4 => "RGBA",
                        _ => "Base Color"
                    };
                    return BuildOutputInput(output, pinName, outputPin.Type, DefaultForType(outputPin.Type));
                }
            }
            finally
            {
                _graph = previousGraph;
            }
        }

        // Passthrough seguro para subgraphs vacíos o en construcción.
        var input = outputPin.Type switch
        {
            PinType.Float => node.Input("Float In") ?? node.Input("In"),
            PinType.Vec2 => node.Input("Vec2 In") ?? node.Input("In"),
            PinType.Vec4 => node.Input("Vec4 In") ?? node.Input("In"),
            _ => node.Input("Vec3 In") ?? node.Input("In")
        };
        if (input is null)
            return DefaultForType(outputPin.Type);

        var type = HighestNumericType(ResolveInputType(input), outputPin.Type);
        return CastExpression(BuildInput(input, type), type, outputPin.Type);
    }

