    private PinType ResolveInputType(GraphPin? inputPin)
    {
        if (inputPin is null)
            return PinType.Float;

        var connection = _graph.FindConnectionToInput(inputPin.Id);
        if (connection is null)
            return inputPin.Type;

        var sourcePin = _graph.FindPin(connection.FromPinId);
        var sourceNode = _graph.FindNodeByPin(connection.FromPinId);
        if (sourcePin is null || sourceNode is null)
            return inputPin.Type;

        return ResolveOutputType(sourceNode, sourcePin);
    }

    private PinType ResolveOutputType(ShaderNode sourceNode, GraphPin sourcePin)
    {
        if (sourceNode.Kind is NodeKind.Add or NodeKind.Subtract or NodeKind.Multiply or NodeKind.Divide or NodeKind.Min or NodeKind.Max or NodeKind.Lerp)
        {
            var a = sourceNode.Input("A");
            var b = sourceNode.Input("B");
            if (a is not null && b is not null)
                return HighestNumericType(ResolveInputType(a), ResolveInputType(b), sourcePin.Type);
        }

        if (sourceNode.Kind == NodeKind.MultiplyAdd)
        {
            var a = sourceNode.Input("A");
            var b = sourceNode.Input("B");
            var c = sourceNode.Input("C");
            if (a is not null && b is not null && c is not null)
                return HighestNumericType(ResolveInputType(a), ResolveInputType(b), ResolveInputType(c), sourcePin.Type);
        }

        if (sourceNode.Kind is NodeKind.Sin or NodeKind.Cos or NodeKind.OneMinus or NodeKind.Saturate
            or NodeKind.Step or NodeKind.Abs or NodeKind.Floor or NodeKind.Ceil or NodeKind.Fraction
            or NodeKind.Negate or NodeKind.Reciprocal)
        {
            var x = InputAny(sourceNode, "In", "X");
            if (x is not null)
                return HighestNumericType(ResolveInputType(x), sourcePin.Type);
        }

        if (sourceNode.Kind == NodeKind.Mix)
        {
            var a = sourceNode.Input("A");
            var b = sourceNode.Input("B");
            if (a is not null && b is not null)
                return HighestNumericType(ResolveInputType(a), ResolveInputType(b), PinType.Vec3);
        }

        if (sourceNode.Kind == NodeKind.SubGraph)
        {
            var input = sourcePin.Type switch
            {
                PinType.Float => sourceNode.Input("Float In") ?? sourceNode.Input("In"),
                PinType.Vec2 => sourceNode.Input("Vec2 In") ?? sourceNode.Input("In"),
                PinType.Vec4 => sourceNode.Input("Vec4 In") ?? sourceNode.Input("In"),
                _ => sourceNode.Input("Vec3 In") ?? sourceNode.Input("In")
            };
            if (input is not null)
                return HighestNumericType(ResolveInputType(input), sourcePin.Type);
        }

        return sourcePin.Type;
    }

    private static PinType HighestNumericType(params PinType[] types)
    {
        var rank = types.Select(t => t switch
        {
            PinType.Vec4 => 4,
            PinType.Vec3 => 3,
            PinType.Vec2 => 2,
            _ => 1
        }).Max();

        return rank switch
        {
            4 => PinType.Vec4,
            3 => PinType.Vec3,
            2 => PinType.Vec2,
            _ => PinType.Float
        };
    }

