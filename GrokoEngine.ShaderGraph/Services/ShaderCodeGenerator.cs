    private static string CastExpression(string expr, PinType from, PinType to)
    {
        expr = NormalizeDefaultExpression(expr, from);
        if (from == to)
            return expr;

        return (from, to) switch
        {
            (PinType.Float, PinType.Vec2) => $"vec2({expr})",
            (PinType.Float, PinType.Vec3) => $"vec3({expr})",
            (PinType.Float, PinType.Vec4) => $"vec4(vec3({expr}), 1.0)",
            (PinType.Vec2, PinType.Float) => $"({expr}).x",
            (PinType.Vec2, PinType.Vec3) => $"vec3({expr}, 0.0)",
            (PinType.Vec2, PinType.Vec4) => $"vec4({expr}, 0.0, 1.0)",
            (PinType.Vec3, PinType.Float) => $"dot({expr}, vec3(0.2126, 0.7152, 0.0722))",
            (PinType.Vec3, PinType.Vec2) => $"({expr}).xy",
            (PinType.Vec3, PinType.Vec4) => $"vec4({expr}, 1.0)",
            (PinType.Vec4, PinType.Float) => $"({expr}).x",
            (PinType.Vec4, PinType.Vec2) => $"({expr}).xy",
            (PinType.Vec4, PinType.Vec3) => $"({expr}).rgb",
            _ => expr
        };
    }

    private static string NormalizeDefaultExpression(string expr, PinType type)
    {
        expr = string.IsNullOrWhiteSpace(expr) ? DefaultForType(type) : expr.Trim();
        if (expr.Equals("v_UV", StringComparison.OrdinalIgnoreCase))
            return "v_UV";
        if (expr.Equals("v_UV.x", StringComparison.OrdinalIgnoreCase))
            return "v_UV.x";
        if (expr.Equals("v_UV.y", StringComparison.OrdinalIgnoreCase))
            return "v_UV.y";
        return expr;
    }

    private static string DefaultForType(PinType type) => type switch
    {
        PinType.Float => "0.0",
        PinType.Vec2 => "vec2(0.0)",
        PinType.Vec3 => "vec3(0.0)",
        PinType.Vec4 => "vec4(0.0)",
        PinType.Texture2D => "u_MainTex",
        _ => "0.0"
    };

    private static string ToVec3(string colorHex, float intensity = 1f)
    {
        intensity = NormalizeHdrIntensity(intensity);

        try
        {
            var parsed = MediaColorConverter.ConvertFromString(colorHex);
            if (parsed is MediaColor c)
            {
                var r = c.R / 255f * intensity;
                var g = c.G / 255f * intensity;
                var b = c.B / 255f * intensity;
                return $"vec3({F(r)}, {F(g)}, {F(b)})";
            }
        }
        catch
        {
            // Use magenta when the color is invalid.
        }

        return $"vec3({F(intensity)}, 0.0, {F(intensity)})";
    }

    private static float NormalizeHdrIntensity(float intensity)
    {
        if (float.IsNaN(intensity) || float.IsInfinity(intensity) || intensity <= 0f)
            return 1f;

        return Math.Clamp(intensity, 0.05f, 64f);
    }

    private static string ToVec2(string text)
    {
        var values = ParseFloats(text, 2);
        return $"vec2({F(values[0])}, {F(values[1])})";
    }

    private static string ToVec3FromText(string text)
    {
        var values = ParseFloats(text, 3);
        return $"vec3({F(values[0])}, {F(values[1])}, {F(values[2])})";
    }

    private static float[] ParseFloats(string text, int count)
    {
        var result = Enumerable.Repeat(0f, count).ToArray();
        if (string.IsNullOrWhiteSpace(text))
            return result;

        var clean = text.Replace("vec2", string.Empty, StringComparison.OrdinalIgnoreCase)
                        .Replace("vec3", string.Empty, StringComparison.OrdinalIgnoreCase)
                        .Replace("(", string.Empty)
                        .Replace(")", string.Empty);
        var parts = clean.Split(new[] { ',', ';', ' ' }, StringSplitOptions.TrimEntries | StringSplitOptions.RemoveEmptyEntries);
        for (var i = 0; i < Math.Min(count, parts.Length); i++)
        {
            if (float.TryParse(parts[i], NumberStyles.Float, CultureInfo.InvariantCulture, out var value))
                result[i] = value;
        }
        return result;
    }

    private static string F(float value)
        => value.ToString("0.######", CultureInfo.InvariantCulture);

    private static string ToHlslUniformField(string glslUniformLine)
    {
        var line = glslUniformLine.Trim().TrimEnd(';');
        if (line.StartsWith("uniform ", StringComparison.OrdinalIgnoreCase))
            line = line[8..];

        line = line.Replace("vec2", "float2", StringComparison.Ordinal)
                   .Replace("vec3", "float3", StringComparison.Ordinal)
                   .Replace("vec4", "float4", StringComparison.Ordinal);

        return line + ";";
    }

    private static string ToHlslExpression(string expr)
    {
        expr = expr.Replace("vec2", "float2", StringComparison.Ordinal)
                   .Replace("vec3", "float3", StringComparison.Ordinal)
                   .Replace("vec4", "float4", StringComparison.Ordinal)
                   .Replace("mix(", "lerp(", StringComparison.Ordinal)
                   .Replace("fract(", "frac(", StringComparison.Ordinal)
                   .Replace("mod(", "fmod(", StringComparison.Ordinal);

        expr = Regex.Replace(expr, @"texture\s*\(\s*([A-Za-z_][A-Za-z0-9_]*)\s*,", "$1.Sample(GrokoLinearSampler, ");
        return expr;
    }

    private void AppendHlslFeatureFunctions(StringBuilder sb)
    {
        sb.AppendLine("float3 fresnelSchlick(float cosTheta, float3 F0) { return F0 + (1.0 - F0) * pow(1.0 - saturate(cosTheta), 5.0); }");
        sb.AppendLine("float DistributionGGX(float3 N, float3 H, float roughness) { float a = roughness * roughness; float a2 = a * a; float NdotH = saturate(dot(N, H)); float NdotH2 = NdotH * NdotH; float denom = (NdotH2 * (a2 - 1.0) + 1.0); return a2 / max(3.14159265 * denom * denom, 0.0001); }");
        sb.AppendLine("float GeometrySchlickGGX(float NdotV, float roughness) { float r = roughness + 1.0; float k = (r * r) / 8.0; return NdotV / max(NdotV * (1.0 - k) + k, 0.0001); }");
        sb.AppendLine("float GeometrySmith(float3 N, float3 V, float3 L, float roughness) { float ggx2 = GeometrySchlickGGX(saturate(dot(N, V)), roughness); float ggx1 = GeometrySchlickGGX(saturate(dot(N, L)), roughness); return ggx1 * ggx2; }");
        sb.AppendLine("float3 applyNormalMap(float3 normalWS, float3 tangentWS, float3 sampleRGB, float strength) { float3 n = normalize(normalWS); float3 t = normalize(tangentWS - n * dot(tangentWS, n)); if (length(t) < 0.001) t = normalize(abs(n.y) > 0.99 ? float3(1.0, 0.0, 0.0) : cross(float3(0.0, 1.0, 0.0), n)); float3 b = normalize(cross(n, t)); float3x3 TBN = float3x3(t, b, n); float3 tsn = normalize(sampleRGB * 2.0 - 1.0); tsn.xy *= strength; return normalize(mul(tsn, TBN)); }");
        if (_features.Contains("noise") || _features.Contains("voronoi") || _features.Contains("gradientnoise"))
        {
            sb.AppendLine("float hash(float2 p) { p = frac(p * float2(123.34, 456.21)); p += dot(p, p + 45.32); return frac(p.x * p.y); }");
            sb.AppendLine("float2 hash2(float2 p) { return frac(sin(float2(dot(p, float2(127.1, 311.7)), dot(p, float2(269.5, 183.3)))) * 43758.5453); }");
            sb.AppendLine();
        }

        if (_features.Contains("gradientnoise"))
        {
            sb.AppendLine("float gradientNoise(float2 p)");
            sb.AppendLine("{");
            sb.AppendLine("    float2 i = floor(p);");
            sb.AppendLine("    float2 f = frac(p);");
            sb.AppendLine("    float2 u = f * f * (3.0 - 2.0 * f);");
            sb.AppendLine("    float2 ga = hash2(i) * 2.0 - 1.0;");
            sb.AppendLine("    float2 gb = hash2(i + float2(1.0, 0.0)) * 2.0 - 1.0;");
            sb.AppendLine("    float2 gc = hash2(i + float2(0.0, 1.0)) * 2.0 - 1.0;");
            sb.AppendLine("    float2 gd = hash2(i + float2(1.0, 1.0)) * 2.0 - 1.0;");
            sb.AppendLine("    float va = dot(ga, f - float2(0.0, 0.0));");
            sb.AppendLine("    float vb = dot(gb, f - float2(1.0, 0.0));");
            sb.AppendLine("    float vc = dot(gc, f - float2(0.0, 1.0));");
            sb.AppendLine("    float vd = dot(gd, f - float2(1.0, 1.0));");
            sb.AppendLine("    return 0.5 + lerp(lerp(va, vb, u.x), lerp(vc, vd, u.x), u.y);");
            sb.AppendLine("}");
            sb.AppendLine();
        }

        if (_features.Contains("checkerboard"))
        {
            sb.AppendLine("float checkerboard(float2 uv, float2 frequency) { float2 c = floor(uv * frequency); return fmod(c.x + c.y, 2.0); }");
            sb.AppendLine();
        }

        if (_features.Contains("scenedepth"))
        {
            sb.AppendLine("float sceneDepthLinearEye(float raw) { float ndcZ = raw * 2.0 - 1.0; return (2.0 * u_CameraNear * u_CameraFar) / (u_CameraFar + u_CameraNear - ndcZ * (u_CameraFar - u_CameraNear)); }");
            sb.AppendLine("float sceneDepthLinear01(float raw) { return (sceneDepthLinearEye(raw) - u_CameraNear) / (u_CameraFar - u_CameraNear); }");
            sb.AppendLine("float depthFade(float2 screenUV, float fadeDistance, float fragDepth) { float sceneEye = sceneDepthLinearEye(u_SceneDepth.Sample(GrokoLinearSampler, screenUV).r); float surfaceEye = sceneDepthLinearEye(fragDepth); return saturate((sceneEye - surfaceEye) / max(fadeDistance, 0.0001)); }");
            sb.AppendLine();
        }

        if (_features.Contains("triplanar"))
        {
            sb.AppendLine("float3 triplanar(Texture2D tex, float3 worldPos, float3 normalWS, float scale, float blendSharpness)");
            sb.AppendLine("{");
            sb.AppendLine("    float3 blend = pow(abs(normalWS), float3(blendSharpness, blendSharpness, blendSharpness));");
            sb.AppendLine("    blend /= max(blend.x + blend.y + blend.z, 0.0001);");
            sb.AppendLine("    float3 x = tex.Sample(GrokoLinearSampler, worldPos.yz * scale).rgb;");
            sb.AppendLine("    float3 y = tex.Sample(GrokoLinearSampler, worldPos.xz * scale).rgb;");
            sb.AppendLine("    float3 z = tex.Sample(GrokoLinearSampler, worldPos.xy * scale).rgb;");
            sb.AppendLine("    return x * blend.x + y * blend.y + z * blend.z;");
            sb.AppendLine("}");
            sb.AppendLine();
        }

        if (_features.Contains("noise"))
        {
            sb.AppendLine("float noise(float2 p)");
            sb.AppendLine("{");
            sb.AppendLine("    float2 i = floor(p);");
            sb.AppendLine("    float2 f = frac(p);");
            sb.AppendLine("    float a = hash(i);");
            sb.AppendLine("    float b = hash(i + float2(1.0, 0.0));");
            sb.AppendLine("    float c = hash(i + float2(0.0, 1.0));");
            sb.AppendLine("    float d = hash(i + float2(1.0, 1.0));");
            sb.AppendLine("    float2 u = f * f * (3.0 - 2.0 * f);");
            sb.AppendLine("    return lerp(a, b, u.x) + (c - a) * u.y * (1.0 - u.x) + (d - b) * u.x * u.y;");
            sb.AppendLine("}");
            sb.AppendLine();
        }

        if (_features.Contains("voronoi"))
        {
            sb.AppendLine("float voronoi(float2 p, float angleOffset)");
            sb.AppendLine("{");
            sb.AppendLine("    float2 i = floor(p);");
            sb.AppendLine("    float2 f = frac(p);");
            sb.AppendLine("    float minDist = 8.0;");
            sb.AppendLine("    [unroll] for (int y = -1; y <= 1; y++)");
            sb.AppendLine("    {");
            sb.AppendLine("        [unroll] for (int x = -1; x <= 1; x++)");
            sb.AppendLine("        {");
            sb.AppendLine("            float2 neighbor = float2((float)x, (float)y);");
            sb.AppendLine("            float2 point = hash2(i + neighbor);");
            sb.AppendLine("            point = 0.5 + 0.5 * sin(angleOffset + 6.2831 * point);");
            sb.AppendLine("            float2 diff = neighbor + point - f;");
            sb.AppendLine("            minDist = min(minDist, dot(diff, diff));");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("    return sqrt(minDist);");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("float voronoiCells(float2 p, float angleOffset)");
            sb.AppendLine("{");
            sb.AppendLine("    float2 i = floor(p);");
            sb.AppendLine("    float2 f = frac(p);");
            sb.AppendLine("    float minDist = 8.0;");
            sb.AppendLine("    float2 minCell = float2(0.0, 0.0);");
            sb.AppendLine("    [unroll] for (int y = -1; y <= 1; y++)");
            sb.AppendLine("    {");
            sb.AppendLine("        [unroll] for (int x = -1; x <= 1; x++)");
            sb.AppendLine("        {");
            sb.AppendLine("            float2 neighbor = float2((float)x, (float)y);");
            sb.AppendLine("            float2 point = hash2(i + neighbor);");
            sb.AppendLine("            point = 0.5 + 0.5 * sin(angleOffset + 6.2831 * point);");
            sb.AppendLine("            float2 diff = neighbor + point - f;");
            sb.AppendLine("            float dist = dot(diff, diff);");
            sb.AppendLine("            if (dist < minDist) { minDist = dist; minCell = i + neighbor; }");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("    return hash(minCell);");
            sb.AppendLine("}");
            sb.AppendLine();
        }

        if (_graph.Nodes.Any(n => n.Kind == NodeKind.Flipbook))
        {
            sb.AppendLine("float2 flipbookUV(float2 uv, float columns, float rows, float frame) { float total = max(columns * rows, 1.0); float f = fmod(floor(frame), total); float x = fmod(f, columns); float y = floor(f / columns); return (uv + float2(x, rows - 1.0 - y)) / float2(columns, rows); }");
            sb.AppendLine();
        }

        if (_graph.Nodes.Any(n => n.Kind == NodeKind.Rotator))
        {
            sb.AppendLine("float2 rotateUV(float2 uv, float angle) { float s = sin(angle); float c = cos(angle); uv -= float2(0.5, 0.5); float2 r = float2(c * uv.x - s * uv.y, s * uv.x + c * uv.y); return r + float2(0.5, 0.5); }");
            sb.AppendLine();
        }

        if (HasOverlayBlend())
        {
            sb.AppendLine("float3 blendOverlay(float3 a, float3 b) { return lerp(2.0 * a * b, 1.0 - 2.0 * (1.0 - a) * (1.0 - b), step(0.5, a)); }");
            sb.AppendLine();
        }

        if (_features.Contains("dissolve"))
        {
            sb.AppendLine("float dissolveAlpha(float mask, float amount, float edgeWidth) { float ew = max(edgeWidth, 0.0001); return smoothstep(amount, amount + ew, mask); }");
            sb.AppendLine("float dissolveEdge(float mask, float amount, float edgeWidth) { float ew = max(edgeWidth, 0.0001); return (1.0 - smoothstep(amount + ew, amount + ew * 2.0, mask)) * smoothstep(amount, amount + ew, mask); }");
            sb.AppendLine();
        }

        if (_graph.Nodes.Any(n => n.Kind == NodeKind.Twirl))
        {
            sb.AppendLine("float2 twirlUV(float2 uv, float2 center, float strength) { float2 delta = uv - center; float dist = length(delta); float angle = strength * (1.0 - smoothstep(0.0, 1.0, dist)); float s = sin(angle); float c = cos(angle); return center + float2(c * delta.x - s * delta.y, s * delta.x + c * delta.y); }");
            sb.AppendLine();
        }

        if (_graph.Nodes.Any(n => n.Kind == NodeKind.PolarCoordinates))
        {
            sb.AppendLine("float2 polarUV(float2 uv, float2 center, float radialScale, float angularScale) { float2 d = uv - center; float r = length(d) * radialScale; float a = atan2(d.y, d.x) / 6.2831853 + 0.5; return float2(r, frac(a * angularScale)); }");
            sb.AppendLine();
        }
