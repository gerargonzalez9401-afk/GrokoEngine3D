    private string BuildSwizzle(ShaderNode node, GraphPin outputPin)
    {
        var input = BuildInput(node.Input("In")!, PinType.Vec4);
        var raw = (node.TextValue ?? "rgba").Trim().ToLowerInvariant();

        // Normaliza xyzw -> rgba para evitar swizzles mixtos inválidos en GLSL (p.ej. ".rxyz").
        var xyzwToRgba = new Dictionary<char, char> { ['x'] = 'r', ['y'] = 'g', ['z'] = 'b', ['w'] = 'a' };
        var pattern = new string(raw
            .Where(c => "rgbaxyzw".Contains(c))
            .Select(c => xyzwToRgba.TryGetValue(c, out var mapped) ? mapped : c)
            .ToArray());
        if (string.IsNullOrEmpty(pattern))
            pattern = "rgba";

        if (outputPin.Name.Equals("X", StringComparison.OrdinalIgnoreCase))
            return $"({input}).{pattern[0]}";
        if (outputPin.Name.Equals("RGB", StringComparison.OrdinalIgnoreCase))
        {
            if (pattern.Length >= 3)
                return $"({input}).{pattern[..3]}";

            // Patrón corto (1-2 canales): respeta el swizzle pedido y rellena el resto con 0.0.
            var rgbDefaults = new[] { "0.0", "0.0" };
            var rgbList = pattern.Select(c => $"({input}).{c}").Concat(rgbDefaults).Take(3);
            return $"vec3({string.Join(", ", rgbList)})";
        }
        if (pattern.Length >= 4)
            return $"({input}).{pattern[..4]}";

        // Constructor vec4 con el número correcto de componentes (rellena con 0.0 / alpha 1.0).
        var defaults = new[] { "0.0", "0.0", "0.0", "1.0" };
        var components = pattern.Select(c => $"({input}).{c}").Concat(defaults.Skip(pattern.Length)).Take(4);
        return $"vec4({string.Join(", ", components)})";
    }

    private string BuildAppend(ShaderNode node, GraphPin outputPin)
    {
        var aPin = node.Input("A")!;
        var aType = ResolveInputType(aPin);
        var b = BuildInput(node.Input("B")!, PinType.Float);
        var wantsVec3 = outputPin.Name.Equals("Vec3", StringComparison.OrdinalIgnoreCase);

        switch (aType)
        {
            case PinType.Vec2:
            {
                var a2 = BuildInput(aPin, PinType.Vec2);
                return wantsVec3 ? $"vec3({a2}, {b})" : $"vec4({a2}, {b}, 1.0)";
            }
            case PinType.Float:
            {
                var a1 = BuildInput(aPin, PinType.Float);
                return wantsVec3 ? $"vec3({a1}, {b}, 0.0)" : $"vec4({a1}, {b}, 0.0, 1.0)";
            }
            default:
            {
                var a3 = BuildInput(aPin, PinType.Vec3);
                return wantsVec3 ? a3 : $"vec4({a3}, {b})";
            }
        }
    }

